# Crie um programa que leia um número REAL qualquer pelo teclado e mostre na tela a sua porção inteira.
import math

número = float(input("digite um valor: "))
print(f"o valor digitaado foi {número} e sua porção inteira é {math.trunc(número)}")

número = int(input("digite um valor: "))
print(f"o valor digitaado foi {número} e sua porção inteira é {math.sqrt(número)}")
